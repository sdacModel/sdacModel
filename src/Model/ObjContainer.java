package Model;

/**
 *
 * @author tuankqhe130156
 */
public class ObjContainer {
    int id;
    String name;

    public ObjContainer() {
    }

    public ObjContainer(int id, String name ) {
        this.id = id;
        this.name = name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return name;
    }
    
    
}
